#  1. INTEGER n
#  2. INTEGER_ARRAY ar
#

def sockMerchant(n, ar):
    # takes n, the number of socks, and ar, a list of socks of different colours, and returns the number of pairs of socks, paired by colour
    colours = list(set(ar))
    pairs = 0
    for colour in colours:
        count = ar.count(colour)
        pairs += count//2
    return pairs

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    ar = list(map(int, input().rstrip().split()))

    result = sockMerchant(n, ar)

    fptr.write(str(result) + '\n')

    fptr.close()
